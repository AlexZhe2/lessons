package lesson20.task;

import lesson20.task.diContainer.ConfigClass;

@ConfigClass(prefix="server")
public class ServerSettings {
    private String ip;
    private int port;

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }
}
